class PaintingRobot:
    '''
    position (x, y)
    oritentation: (0, 1) = up, (1, 0) = right, (0, -1) = down, (-1, 0) = left
    '''

    directions = {0: (0, 1), 1: (1, 0), 2: (0, -1), 3: (-1, 0)}

    def __init__(self, start=(0, 0), orientation=0):
        self.position = start
        self.orientation = orientation

    def rotate(self, rotate: int):
        if rotate == 1:  # right
            self.orientation = (self.orientation + 1) % 4
        else:  # left
            self.orientation = (self.orientation - 1) % 4

    def move(self):
        step = self.directions[self.orientation]
        self.position = (self.position[0] + step[0], self.position[1] + step[1])

    def whereami(self):
        return self.position


